% Export forced-choice agreement (each model x each participant) as APA LaTeX.
% Run from results/ or any cwd; uses models/storage hierarchical entrop fits.

thisDir = fileparts(mfilename('fullpath'));
modelsDir = fullfile(thisDir, '..', 'models');
generalDir = fullfile(thisDir, '..', 'general');
storageDir = fullfile(modelsDir, 'storage');
manuscriptsDir = fullfile(thisDir, '..', 'manuscripts');
addpath(modelsDir);
addpath(generalDir);

dataName = 'intertemporalChoice';
engine = 'jags';
choiceThreshold = 0.5;

[data, d] = prepareIntertemporalChoiceData(dataName);
nP = double(d.nParticipants);
nT = double(d.nTrials);

modelShort = {'Ex', 'Hc', 'Hd', 'PD', 'DD', 'Tr', 'UT', 'IT', 'Gu', 'LL', 'SS'};
nModels = numel(modelShort);

cognitiveStems = { ...
  'exponentialExecutionHierarchical_entrop', ...
  'hyperbolicExecutionHierarchical_entrop', ...
  'hyperboloidExecutionHierarchical_entrop', ...
  'proportionalDifferencesExecutionHierarchical_entrop', ...
  'directDifferencesExecutionHierarchical_entrop', ...
  'tradeoffExecutionHierarchical_entrop', ...
  'unifiedTradeoffExecutionHierarchical_entrop', ...
  'itchExecutionHierarchical_entrop'};
cognitiveJagsNames = cognitiveStems;
contaminantStems = {'guess', 'LL', 'SS'};

% MAP model index per participant (from latent-mixture summary)
mapSummaryPath = fullfile(storageDir, ...
  sprintf('mapModelForcedChoiceMatch_summary_origMAP_original_%s_%s.mat', ...
    dataName, engine));
if ~isfile(mapSummaryPath)
  mapSummaryPath = fullfile(storageDir, ...
    sprintf('mapModelForcedChoiceMatch_summary_%s_%s_%s.mat', ...
      'latentMixtureHierarchicalPrecision_entrop', dataName, engine));
end
if ~isfile(mapSummaryPath)
  error('Missing MAP summary (needed to bold MAP cells): tried origMAP_original and mixture summaries');
end
mapS = load(mapSummaryPath, 'mapModel');
mapModel = mapS.mapModel(:)';
if numel(mapModel) ~= nP
  error('mapModel length %d does not match nP=%d', numel(mapModel), nP);
end
fprintf('Loaded MAP models from %s\n', mapSummaryPath);

matchProp = nan(nModels, nP);

for mi = 1:nModels
  if mi <= 8
    stem = cognitiveStems{mi};
  else
    stem = contaminantStems{mi - 8};
  end
  fpath = fullfile(storageDir, sprintf('%s_%s_%s.mat', stem, dataName, engine));
  if ~isfile(fpath)
    warning('Missing %s', fpath);
    continue;
  end
  fprintf('Loading %s\n', fpath);
  L = load(fpath, 'chains');
  chains = L.chains;
  for pp = 1:nP
    meanTh = mapModelMeanThetaLocal(chains, data, cognitiveJagsNames, mi, pp, nT);
    if isempty(meanTh) || numel(meanTh) ~= nT
      continue;
    end
    obs = d.LL(pp, :);
    forcedLL = nan(size(meanTh));
    forcedLL(meanTh > choiceThreshold) = 1;
    forcedLL(meanTh < choiceThreshold) = 0;
    valid = isfinite(forcedLL) & isfinite(obs);
    if any(valid)
      matchProp(mi, pp) = mean(forcedLL(valid) == obs(valid));
    end
  end
  clear chains L;
end

outTex = fullfile(manuscriptsDir, 'forcedChoiceAgreementTable.tex');
fid = fopen(outTex, 'w');
if fid < 0
  error('Could not write %s', outTex);
end
cleanup = onCleanup(@() fclose(fid));

fprintf(fid, '%% Auto-generated by results/exportForcedChoiceAgreementTable.m — do not edit by hand.\n');
fprintf(fid, '\\begin{sidewaystable}[htbp]\n');
fprintf(fid, '\\centering\n');
fprintf(fid, '\\caption{Forced-choice posterior predictive agreement (percentage of trials) for each model and participant. ');
fprintf(fid, 'For each trial, the model''s posterior mean $P(\\mathrm{LL})$ was thresholded at $.5$ to yield a forced larger-later or smaller-sooner prediction, ');
fprintf(fid, 'and agreement is the proportion of trials matching the participant''s observed choice. ');
fprintf(fid, '\\textbf{Bold} entries mark the model with the highest posterior probability for that participant in the latent-mixture analysis. ');
fprintf(fid, 'Model abbreviations: Ex~=~exponential, Hc~=~hyperbolic, Hd~=~hyperboloid, PD~=~proportional differences, ');
fprintf(fid, 'DD~=~direct differences, Tr~=~tradeoff, UT~=~unified tradeoff, IT~=~intertemporal choice heuristic, ');
fprintf(fid, 'Gu~=~guess, LL~=~larger-later contaminant, SS~=~smaller-sooner contaminant.}\n');
fprintf(fid, '\\label{tab:forcedChoiceAgreement}\n');
fprintf(fid, '\\begin{adjustbox}{max width=\\textheight}\n');
fprintf(fid, '\\begin{tabular}{l%s}\n', repmat('r', 1, nModels));
fprintf(fid, '\\toprule\n');
fprintf(fid, 'Participant');
for mi = 1:nModels
  fprintf(fid, ' & %s', modelShort{mi});
end
fprintf(fid, ' \\\\\n\\midrule\n');

for pp = 1:nP
  fprintf(fid, '%s', char('A' + pp - 1));
  miMap = mapModel(pp);
  for mi = 1:nModels
    v = matchProp(mi, pp);
    if ~isfinite(v)
      cellStr = '---';
    else
      cellStr = sprintf('%.0f', round(100 * v));
    end
    if isfinite(miMap) && mi == miMap
      fprintf(fid, ' & \\textbf{%s}', cellStr);
    else
      fprintf(fid, ' & %s', cellStr);
    end
  end
  fprintf(fid, ' \\\\\n');
end

fprintf(fid, '\\bottomrule\n');
fprintf(fid, '\\end{tabular}\n');
fprintf(fid, '\\end{adjustbox}\n');
fprintf(fid, '\\end{sidewaystable}\n');

fprintf('Wrote %s\n', outTex);
csvPath = fullfile(thisDir, 'descriptiveAdequacy', 'forcedChoiceAgreement_allModels.csv');
if ~isfolder(fileparts(csvPath))
  mkdir(fileparts(csvPath));
end
hdr = [{'Participant'}, modelShort];
rows = cell(nP, nModels + 1);
for pp = 1:nP
  rows{pp, 1} = char('A' + pp - 1);
  for mi = 1:nModels
    rows{pp, mi + 1} = matchProp(mi, pp);
  end
end
writecell([hdr; rows], csvPath);
fprintf('Wrote %s\n', csvPath);

function meanTh = mapModelMeanThetaLocal(chains, data, cognitiveJagsNames, mi, pp, nT)
if mi <= 8
  Th = hierarchicalExecutionThetaDraws(chains, data, cognitiveJagsNames{mi}, pp);
  if isempty(Th)
    meanTh = [];
  else
    meanTh = mean(Th, 1);
  end
  return;
end
meanTh = nan(1, nT);
for j = 1:nT
  fn = sprintf('theta_%d_%d', pp, j);
  if ~isfield(chains, fn)
    meanTh = [];
    return;
  end
  meanTh(j) = mean(chains.(fn)(:));
end
end
